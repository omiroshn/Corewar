#include "libft.h"

int		ft_isnum(char *s)
{
	size_t i;

	i = 0;
	if (*s == '-')
		++i;
	while (s && s[i])
	{
		if (ft_isdigit(s[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
