#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	register size_t	i;

	if (!s1 || !s2)
		return (s1);
	i = 0;
	while (i < n)
	{
		((char *)s1)[i] = ((char *)s2)[i];
		i++;
	}
	return (s1);
}
