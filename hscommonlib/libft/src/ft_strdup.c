#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(char *s1)
{
	char		*res;

	if ((res = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
	{
		ft_strcpy(res, s1);
		return (res);
	}
	else
		return (NULL);
}
